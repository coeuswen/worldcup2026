def q(s):
    """Safe quote: wrap in triple quotes if contains newlines or quotes"""
    if '\n' in s or '"' in s or '<' in s:
        return '"""' + s + '"""'
    if isinstance(s, str):
        return '"' + s.replace('\\', '\\\\').replace('"', '\\"') + '"'
    return str(s)

def dict_to_py(d, indent=0):
    """Convert a Python dict to formatted Python source code"""
    lines = []
    prefix = " " * indent
    inner = " " * (indent + 4)
    lines.append(prefix + "{")
    items = list(d.items())
    for j, (k, v) in enumerate(items):
        comma = "," if j < len(items) - 1 else ""
        if isinstance(v, dict):
            lines.append(f'{inner}"{k}": {dict_to_py(v, indent + 4)}{comma}')
        elif isinstance(v, list):
            lines.append(f'{inner}"{k}": [')
            for item in v:
                if isinstance(item, (list, tuple)):
                    items_str = ', '.join(q(x) for x in item)
                    lines.append(f'{inner}    ({items_str}),')
                else:
                    lines.append(f'{inner}    {q(item)},')
            lines.append(f'{inner}]{comma}')
        elif isinstance(v, str):
            lines.append(f'{inner}"{k}": {q(v)}{comma}')
        elif isinstance(v, bool):
            lines.append(f'{inner}"{k}": {str(v)}{comma}')
        elif isinstance(v, float):
            lines.append(f'{inner}"{k}": {v}{comma}')
        else:
            lines.append(f'{inner}"{k}": {v}{comma}')
    lines.append(prefix + "}")
    return "\n".join(lines)


